package com.example.weatherwise.model;

public class HydrationSetting {

    private int hydrationHour;

    private int hydrationMinute;


    public int getHydrationHour() {
        return hydrationHour;
    }

    public void setHydrationHour(int hydrationHour) {
        this.hydrationHour = hydrationHour;
    }

    public int getHydrationMinute() {
        return hydrationMinute;
    }

    public void setHydrationMinute(int hydrationMinute) {
        this.hydrationMinute = hydrationMinute;
    }

}
